/**********************************************
//IIC Start
**********************************************/
static void IIC_Start()
{
    SCL_1;
    delay(5);
    SDA_1;
    delay(5);
    SDA_0;
    delay(5);
    SCL_0;
    delay(5);
}

/**********************************************
//IIC Stop
**********************************************/
static void IIC_Stop()
{
    SCL_0;
    delay(5);
    SDA_0;
    delay(5);
    SCL_1;
    delay(5);
    SDA_1;
    delay(5);
}

/**********************************************
// 通过I2C总线写一个字节
**********************************************/
void Write_IIC_Byte(unsigned char IIC_Byte)
{
    unsigned char i;
    for (i = 0; i < 8; i++)
    {
        if (IIC_Byte & 0x80)
            SDA_1;
        else
            SDA_0;
        delay(5);
        SCL_1;
        delay(5);
        SCL_0;
        delay(5);
        IIC_Byte <<= 1;
    }
    SDA_1;
    delay(5);
    SCL_1;
    delay(5);
    SCL_0;
    delay(5);
}

/*********************OLED写数据************************************/
void OLED_WrDat(unsigned char IIC_Data)
{
    IIC_Start();
    Write_IIC_Byte(0x78); //0111100->Slave address, 0->SA0=0
    Write_IIC_Byte(0x40); //write data
    Write_IIC_Byte(IIC_Data);
    IIC_Stop();
}
/*********************OLED写命令************************************/
void OLED_WrCmd(unsigned char IIC_Command)
{
    IIC_Start();
    Write_IIC_Byte(0x78); //0111100->Slave address, 0->SA0=0
    Write_IIC_Byte(0x00); //write command
    Write_IIC_Byte(IIC_Command);
    IIC_Stop();
}
/*********************OLED 设置坐标************************************/
void oled_set_pos(unsigned char x, unsigned char y)
{
#ifdef USE_GRAPH_RAM
    GlobalRamPointPos = x + y * 128;
#else
    OLED_WrCmd(0xb0 + y); //设置page，范围b0~b7
    OLED_WrCmd(((x & 0xf0) >> 4) | 0x10); //set the upper column address
    OLED_WrCmd((x & 0x0f) | 0x01); //set the lower column address
#endif
}

